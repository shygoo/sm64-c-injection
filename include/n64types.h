#ifndef _N64TYPES_H_
#define _N64TYPES_H_

typedef long long          s64;
typedef int                s32;
typedef short              s16;
typedef char                s8;
typedef unsigned long long u64;
typedef unsigned int       u32;
typedef unsigned short     u16;
typedef unsigned char       u8;

typedef float  f32;
typedef double f64;

#endif // _N64TYPES_H_
